# Encode +0- truth table to decimal
# ================================

POW3 = [3**i for i in range(9)]

def encode_str(tt_str):
    mapping = {'-': 0, '0': 1, '+': 2}
    tt = [mapping[c] for c in tt_str.strip()]
    return sum(tt[i] * POW3[i] for i in range(9))


